
public class Conversao {

	//converte um valor passado em polegadas para centimetros
	public static double polegadasParaCentimetros(double valorPolegadas) {
		return (valorPolegadas * 2.54);
	}
	
	//converte um valor passado em pés para centimentos
	public static double pesParaCentimetros(double valorPes) {
		return (valorPes*30.48);
	}
	
	//converte um valor passado em milhas para kilometros
	public static double milhasParaKilometros(double valorMilhas) {
		return (valorMilhas*1.609);
	}
	
	
	
}
